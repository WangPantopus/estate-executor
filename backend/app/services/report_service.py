# ═════════════════════════════════════════════════════════════════════════════


async def generate_distribution_ledger_pdf(db: AsyncSession, *, matter_id: uuid.UUID) -> bytes:
    """Generate a PDF distribution ledger report."""
    from reportlab.platypus import Spacer

    matter = await _get_matter_with_firm(db, matter_id)
    distributions = await _get_distributions(db, matter_id)
    stakeholders = await _get_stakeholders(db, matter_id)
    firm_name = (matter.firm.name or "") if matter.firm else ""
    report_title = f"Distribution Ledger — Estate of {matter.decedent_name}"

    # Build stakeholder lookup
    sh_lookup = {str(s.id): s.full_name for s in stakeholders}

    buffer = io.BytesIO()
    doc = _create_pdf_doc(report_title, buffer)
    elements: list[Any] = []

    elements.append(_section_heading("Distribution Ledger"))
    elements.append(
        _body_text(f"Estate of {matter.decedent_name} | {len(distributions)} distribution(s)")
    )
    elements.append(Spacer(1, 8))

    if distributions:
        data = [["Date", "Beneficiary / Recipients", "Description", "Amount", "Acknowledged"]]
        for d in distributions:
            # Build recipient list from visible_to stakeholder IDs
            if d.visible_to:
                recipients = ", ".join(
                    sh_lookup.get(str(uid), str(uid)[:8]) for uid in d.visible_to
                )
            else:
                recipients = "All stakeholders"

            # Extract amount if present in subject (e.g. "$150,000")
            amount = "—"
            body_text = d.body or ""
            subject_text = d.subject or ""
            for text in [subject_text, body_text]:
                match = re.search(r"\$[\d,]+\.?\d*", text)
                if match:
                    amount = match.group(0)
                    break

            ack_count = len(d.acknowledged_by or [])
            total_visible = len(d.visible_to or [])
            ack_label = f"{ack_count}/{total_visible}" if total_visible > 0 else f"{ack_count}"

            data.append(
                [
                    _format_date(d.created_at),
                    recipients[:40],
                    (d.subject or "Distribution Notice")[:40],
                    amount,
                    ack_label,
                ]
            )
        elements.append(_styled_table(data, [70, 120, 120, 70, 60]))
    else:
        elements.append(_body_text("No distributions recorded for this matter."))

    def on_page(canvas: Any, doc_obj: Any) -> None:
        _header_footer(canvas, doc_obj, firm_name=firm_name, report_title=report_title)

    doc.build(elements, onFirstPage=on_page, onLaterPages=on_page)
    return buffer.getvalue()


