def _rgb(color: tuple[int, int, int]) -> Any:
    from reportlab.lib.colors import Color

    return Color(color[0] / 255, color[1] / 255, color[2] / 255)


def _create_pdf_doc(title: str, buffer: io.BytesIO) -> Any:
    """Create a ReportLab SimpleDocTemplate with standard margins."""
    from reportlab.lib.pagesizes import letter
    from reportlab.platypus import SimpleDocTemplate

    return SimpleDocTemplate(
        buffer,
        pagesize=letter,
        topMargin=90,
        bottomMargin=60,
        leftMargin=50,
        rightMargin=50,
        title=title,
    )


def _header_footer(canvas: Any, doc: Any, *, firm_name: str, report_title: str) -> None:
    """Draw header and footer on each page."""
    from reportlab.lib.pagesizes import letter

    width, height = letter

    # Header background
    canvas.setFillColorRGB(*[c / 255 for c in NAVY])
    canvas.rect(0, height - 70, width, 70, fill=1, stroke=0)

    # Gold accent line
    canvas.setFillColorRGB(*[c / 255 for c in GOLD])
    canvas.rect(0, height - 73, width, 3, fill=1, stroke=0)

    # Header text
    canvas.setFillColorRGB(1, 1, 1)
    canvas.setFont("Helvetica-Bold", 14)
    canvas.drawString(50, height - 35, firm_name or "Estate Executor")

    canvas.setFont("Helvetica", 9)
    canvas.setFillColorRGB(*[c / 255 for c in GOLD])
    canvas.drawString(50, height - 52, report_title)

    # Date on right
    canvas.setFillColorRGB(1, 1, 1)
    canvas.setFont("Helvetica", 8)
    today_str = date.today().strftime("%B %d, %Y")
    canvas.drawRightString(width - 50, height - 35, today_str)

    # Footer
    canvas.setFillColorRGB(*[c / 255 for c in MUTED_TEXT])
    canvas.setFont("Helvetica", 7)
    canvas.drawString(50, 30, "Generated by Estate Executor OS")
    canvas.drawRightString(width - 50, 30, f"Page {doc.page}")


def _section_heading(text: str) -> Any:
    """Create a styled section heading paragraph."""
    from reportlab.lib.styles import ParagraphStyle
    from reportlab.platypus import Paragraph

    style = ParagraphStyle(
        "SectionHeading",
        fontName="Helvetica-Bold",
        fontSize=12,
        textColor=_rgb(NAVY),
        spaceBefore=18,
        spaceAfter=8,
        borderWidth=0,
        borderPadding=0,
    )
    return Paragraph(text, style)


def _body_text(text: str) -> Any:
    from reportlab.lib.styles import ParagraphStyle
    from reportlab.platypus import Paragraph

    style = ParagraphStyle(
        "BodyText",
        fontName="Helvetica",
        fontSize=9,
        textColor=_rgb(DARK_TEXT),
        leading=13,
        spaceBefore=2,
        spaceAfter=4,
    )
    return Paragraph(text, style)


def _kv_pair(label: str, value: str) -> Any:
    return _body_text(f"<b>{label}:</b> {value}")


def _styled_table(data: list[list[str]], col_widths: list[float] | None = None) -> Any:
    """Create a styled table with navy header."""
    from reportlab.lib import colors
    from reportlab.platypus import Table, TableStyle

    t = Table(data, colWidths=col_widths, repeatRows=1)
    t.setStyle(
        TableStyle(
            [
                ("BACKGROUND", (0, 0), (-1, 0), _rgb(NAVY)),
                ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
                ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
                ("FONTSIZE", (0, 0), (-1, 0), 8),
                ("FONTNAME", (0, 1), (-1, -1), "Helvetica"),
                ("FONTSIZE", (0, 1), (-1, -1), 8),
                ("TEXTCOLOR", (0, 1), (-1, -1), _rgb(DARK_TEXT)),
                ("ALIGN", (0, 0), (-1, -1), "LEFT"),
                ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),
                ("ROWBACKGROUNDS", (0, 1), (-1, -1), [colors.white, _rgb(LIGHT_BG)]),
                ("GRID", (0, 0), (-1, -1), 0.5, _rgb(BORDER)),
                ("TOPPADDING", (0, 0), (-1, -1), 5),
                ("BOTTOMPADDING", (0, 0), (-1, -1), 5),
                ("LEFTPADDING", (0, 0), (-1, -1), 6),
                ("RIGHTPADDING", (0, 0), (-1, -1), 6),
            ]
        )
    )
    return t


def _format_currency(value: Decimal | float | None) -> str:
    if value is None:
        return "—"
    return f"${value:,.2f}"


def _format_date(d: date | datetime | None) -> str:
    if d is None:
        return "—"
    if isinstance(d, datetime):
        return d.strftime("%m/%d/%Y %H:%M")
    return d.strftime("%m/%d/%Y")


def _enum_label(val: Any) -> str:
    if val is None:
        return "—"
    s = val.value if hasattr(val, "value") else str(val)
    return s.replace("_", " ").title()


