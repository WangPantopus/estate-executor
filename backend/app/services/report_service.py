def _create_workbook() -> Any:
    from openpyxl import Workbook

    wb = Workbook()
    return wb


def _style_excel_header(ws: Any, col_count: int) -> None:
    """Style the first row as a navy header with white text."""
    from openpyxl.styles import Alignment, Border, Font, PatternFill, Side

    navy_fill = PatternFill(start_color="1A2332", end_color="1A2332", fill_type="solid")
    white_font = Font(name="Calibri", bold=True, color="FFFFFF", size=10)
    thin_border = Border(
        bottom=Side(style="thin", color="E5E2DB"),
    )

    for col in range(1, col_count + 1):
        cell = ws.cell(row=1, column=col)
        cell.fill = navy_fill
        cell.font = white_font
        cell.alignment = Alignment(horizontal="left", vertical="center")
        cell.border = thin_border


def _auto_width(ws: Any) -> None:
    """Auto-adjust column widths based on content."""
    for col in ws.columns:
        max_length = 0
        col_letter = col[0].column_letter
        for cell in col:
            try:
                if cell.value:
                    max_length = max(max_length, len(str(cell.value)))
            except Exception:
                pass
        ws.column_dimensions[col_letter].width = min(max_length + 4, 50)


def _freeze_header(ws: Any) -> None:
    ws.freeze_panes = "A2"


