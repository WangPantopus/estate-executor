async def generate_matter_summary_pdf(db: AsyncSession, *, matter_id: uuid.UUID) -> bytes:
    """Generate a one-page PDF overview of the estate."""
    from reportlab.platypus import Spacer

    matter = await _get_matter_with_firm(db, matter_id)
    stakeholders = await _get_stakeholders(db, matter_id)
    tasks = await _get_tasks(db, matter_id)
    assets = await _get_assets(db, matter_id)
    deadlines = await _get_deadlines(db, matter_id)
    recent_events = await _get_recent_events(db, matter_id, limit=8)

    firm_name = (matter.firm.name or "") if matter.firm else ""
    report_title = f"Matter Summary — Estate of {matter.decedent_name}"

    buffer = io.BytesIO()
    doc = _create_pdf_doc(report_title, buffer)

    elements: list[Any] = []

    # Matter details
    elements.append(_section_heading("Matter Details"))
    elements.append(_kv_pair("Title", matter.title))
    elements.append(_kv_pair("Decedent", matter.decedent_name))
    elements.append(_kv_pair("Estate Type", _enum_label(matter.estate_type)))
    elements.append(_kv_pair("Jurisdiction", matter.jurisdiction_state))
    elements.append(_kv_pair("Phase", _enum_label(matter.phase)))
    elements.append(_kv_pair("Status", _enum_label(matter.status)))
    if matter.date_of_death:
        elements.append(_kv_pair("Date of Death", _format_date(matter.date_of_death)))
    if matter.estimated_value:
        elements.append(_kv_pair("Estimated Value", _format_currency(matter.estimated_value)))

    # Stakeholders
    elements.append(Spacer(1, 8))
    elements.append(_section_heading("Stakeholders"))
    if stakeholders:
        sh_data = [["Name", "Role", "Email", "Status"]]
        for s in stakeholders:
            sh_data.append(
                [
                    s.full_name,
                    _enum_label(s.role),
                    s.email,
                    _enum_label(s.invite_status),
                ]
            )
        elements.append(_styled_table(sh_data, [140, 100, 180, 70]))

    # Task completion
    elements.append(Spacer(1, 8))
    elements.append(_section_heading("Task Progress"))
    total = len(tasks)
    complete = sum(1 for t in tasks if t.status in (TaskStatus.complete, TaskStatus.waived))
    overdue = sum(
        1
        for t in tasks
        if t.due_date
        and t.due_date < date.today()
        and t.status not in (TaskStatus.complete, TaskStatus.waived, TaskStatus.cancelled)
    )
    pct = round(complete / total * 100) if total > 0 else 0
    elements.append(
        _body_text(f"Total tasks: {total} | Complete: {complete} ({pct}%) | Overdue: {overdue}")
    )

    # Asset summary
    elements.append(Spacer(1, 8))
    elements.append(_section_heading("Asset Summary"))
    total_value = sum((a.current_estimated_value or Decimal(0)) for a in assets)
    elements.append(
        _body_text(
            f"Total assets: {len(assets)} | Estimated value: {_format_currency(total_value)}"
        )
    )

    # Key deadlines
    upcoming = [
        d for d in deadlines if d.status == DeadlineStatus.upcoming and d.due_date >= date.today()
    ][:5]
    if upcoming:
        elements.append(Spacer(1, 8))
        elements.append(_section_heading("Upcoming Deadlines"))
        dl_data = [["Deadline", "Due Date", "Status"]]
        for d in upcoming:
            dl_data.append([d.title, _format_date(d.due_date), _enum_label(d.status)])
        elements.append(_styled_table(dl_data, [250, 100, 80]))

    # Recent activity
    if recent_events:
        elements.append(Spacer(1, 8))
        elements.append(_section_heading("Recent Activity"))
        act_data = [["Date", "Action", "Entity", "Actor"]]
        for ev in recent_events:
            actor_label = _enum_label(ev.actor_type) if ev.actor_type else "—"
            act_data.append(
                [
                    _format_date(ev.created_at),
                    f"{ev.entity_type} {ev.action}",
                    str(ev.entity_id)[:12] + "...",
                    actor_label,
                ]
            )
        elements.append(_styled_table(act_data, [85, 120, 120, 80]))

    def on_page(canvas: Any, doc_obj: Any) -> None:
        _header_footer(canvas, doc_obj, firm_name=firm_name, report_title=report_title)

    doc.build(elements, onFirstPage=on_page, onLaterPages=on_page)
    return buffer.getvalue()


