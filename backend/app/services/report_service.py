async def _get_matter_with_firm(db: AsyncSession, matter_id: uuid.UUID) -> Matter:
    from app.core.exceptions import NotFoundError

    result = await db.execute(
        select(Matter).options(selectinload(Matter.firm)).where(Matter.id == matter_id)
    )
    matter = result.scalar_one_or_none()
    if matter is None:
        raise NotFoundError(detail="Matter not found")
    return matter


async def _get_stakeholders(db: AsyncSession, matter_id: uuid.UUID) -> list[Stakeholder]:
    result = await db.execute(
        select(Stakeholder)
        .where(Stakeholder.matter_id == matter_id)
        .order_by(Stakeholder.role, Stakeholder.full_name)
    )
    return list(result.scalars().all())


async def _get_tasks(db: AsyncSession, matter_id: uuid.UUID) -> list[Task]:
    result = await db.execute(
        select(Task)
        .options(selectinload(Task.assignee), selectinload(Task.completer))
        .where(Task.matter_id == matter_id)
        .order_by(Task.sort_order, Task.created_at)
    )
    return list(result.scalars().all())


async def _get_assets(db: AsyncSession, matter_id: uuid.UUID) -> list[Asset]:
    result = await db.execute(
        select(Asset).where(Asset.matter_id == matter_id).order_by(Asset.asset_type, Asset.title)
    )
    return list(result.scalars().all())


async def _get_deadlines(db: AsyncSession, matter_id: uuid.UUID) -> list[Deadline]:
    result = await db.execute(
        select(Deadline).where(Deadline.matter_id == matter_id).order_by(Deadline.due_date)
    )
    return list(result.scalars().all())


async def _get_recent_events(
    db: AsyncSession,
    matter_id: uuid.UUID,
    limit: int = 10,
) -> list[Event]:
    result = await db.execute(
        select(Event)
        .where(Event.matter_id == matter_id)
        .order_by(Event.created_at.desc())
        .limit(limit)
    )
    return list(result.scalars().all())


async def _get_distributions(db: AsyncSession, matter_id: uuid.UUID) -> list[Communication]:
    result = await db.execute(
        select(Communication)
        .options(selectinload(Communication.sender))
        .where(
            Communication.matter_id == matter_id,
            Communication.type == CommunicationType.distribution_notice,
        )
        .order_by(Communication.created_at)
    )
    return list(result.scalars().all())


